CREATE TABLE `track` (
	`id` INT(11) NOT NULL AUTO_INCREMENT,
	`ip` VARCHAR(15) NOT NULL,
	`deviceName` VARCHAR(32) NOT NULL,
	`addresses` TEXT NOT NULL,
	`timestamp` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	PRIMARY KEY (`id`),
	INDEX `ip` (`ip`)
)
ENGINE=MyISAM
AUTO_INCREMENT=5;
